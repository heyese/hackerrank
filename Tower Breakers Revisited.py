def num_prime_factors(x):
    num_factors = 0
    root = math.sqrt(x)
    for i in primes:
        if i > root:
            break
        while True:
            if x % i == 0:
                num_factors += 1
                x = x // i
            else:
                break
        if x == 1:
            break
    return num_factors

